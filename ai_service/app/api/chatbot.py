"""
api/chatbot.py - AI Chatbot Interface.
This module facilitates real-time interaction with a local LLM (phi3 via Ollama) 
to assist users with application-specific questions, navigation, and role-based 
capability summaries. It implements strict context-aware prompt engineering to 
ensure security and relevance.
"""

from fastapi import APIRouter, HTTPException, Header
from pydantic import BaseModel, Field
from typing import Optional, Dict, Any
import requests
import logging
import os

router = APIRouter()
logger = logging.getLogger(__name__)

# External LLM service configuration
OLLAMA_URL = os.getenv("OLLAMA_HOST")

# ============================================================
# MODELS
# ============================================================

class ChatRequest(BaseModel):
    """
    Data model for a chatbot query.
    
    Attributes:
        query (str): The natural language question from the user.
        context (Optional[Dict[str, Any]]): Metadata including role, username, and current page.
    """
    query: str
    context: Optional[Dict[str, Any]] = Field(default_factory=dict)

class ChatResponse(BaseModel):
    """
    Data model for the chatbot's generated answer.
    
    Attributes:
        response (str): The text generated by the AI.
        model (str): Identifier of the LLM used (e.g., 'phi3').
        context_used (bool): Whether role-based context was applied to the prompt.
    """
    response: str
    model: str
    context_used: bool


# ============================================================
# CONFIGURATION & UTILITIES
# ============================================================

